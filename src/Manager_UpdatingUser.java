    public Manager_UpdatingUser(Manager manager, String updateTp) {
        this.manager = manager;
        this.updateTp = updateTp;
        
        userlist.setColumnIdentifiers(columnName);

        Main.readUserData();
        List<List<String>> users = Main.getUserData();
        
        for(List<String> user : users){
            String Tp = user.get(0).trim();
            if (Tp.equals(updateTp)){
                String name = user.get(1).trim();
                String email = user.get(3).trim();
                String role = user.get(4).trim();
                userlist.addRow(new Object[]{name, email, role});  
            }   
        }
    
        
        initComponents();
    }

