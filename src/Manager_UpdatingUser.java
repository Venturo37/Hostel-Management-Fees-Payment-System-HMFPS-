    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String updateName = updateNAME.getText().trim();
        String updateEmail = updateEMAIL.getText().trim();
        String updateRole = updateROLE.getText().trim();
        
        String updateStatus = manager.updateUser(updateTp, updateName, updateEmail, updateRole);
        
        if (updateStatus.equals("email exist")){
            javax.swing.JOptionPane.showMessageDialog(this, 
            "Email already exist.", 
            "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        } else if (updateStatus.equals("invalid role")){
            javax.swing.JOptionPane.showMessageDialog(this, 
            "Invalid role. Update rejected.", 
            "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        } else if (updateStatus.equals("update success")){
            JOptionPane.showMessageDialog(this, "Update Successful.");
            Manager_UpdateUser manager_UpdateUser = new Manager_UpdateUser(manager);
            manager_UpdateUser.setVisible(true);
            this.dispose();  
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

